   Alte exemple:
   <p><a href="index.php?cauta_text=garsoniera+detalii%3Adecomandata&c=dm&m=cauta">garsoniera detalii:decomandata</a></p>
   <p><a href="index.php?cauta_text=garsoniera+detalii%3Amobilata&c=dm&m=cauta">garsoniera detalii:mobilata</a></p>

   </div>
