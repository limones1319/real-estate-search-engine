  <p id="cum_caut">
    <h3 class="titlu_intrebare">Cum caut?</h3>
    <div class="raspuns_intrebare">
      Puteti folosi expresii simple de cautare ca:
      <p><a href="index.php?cauta_text=garsoniere&amp;c=dm&amp;m=cauta">garsoniere</a></p>
      <p><a href="index.php?cauta_text=2+camere&amp;c=dm&amp;m=cauta">2 camere</a></p>
      <p><a href="index.php?cauta_text=3+camere&amp;c=dm&amp;m=cauta">3 camere</a></p>
      <p><a href="index.php?cauta_text=3+camere+pantelimon&amp;c=dm&amp;m=cauta">3 camere pantelimon</a></p>
      <p><a href="index.php?cauta_text=garsoniera+obor&amp;c=dm&amp;m=cauta">garsoniera obor</a></p>
    </div>
  </p>
  <p id="anunturi_particulari">
    <h3 class="titlu_intrebare">Cum caut anunturi imobiliare publicate doar de particulari?</h3>
    <div class="raspuns_intrebare">
      Aditional, la expresiile de mai sus se poate adauga cuvantul "particular". In felul acesta veti gasi anunturi imobiliare publicate doar de particulari:
      <p><a href="index.php?cauta_text=3+camere+pantelimon+particular&amp;c=dm&amp;m=cauta">3 camere pantelimon particular</a></p>
      <p><a href="index.php?cauta_text=garsoniera+obor+particular&amp;c=dm&amp;m=cauta">garsoniera obor particular</a></p>
    </div>
  </p>
  <p id="variatia_preturilor">
    <h3 class="titlu_intrebare">Pot vedea evolutia preturilor in timp?</h3>
    <div class="raspuns_intrebare">
      La fiecare cautare, veti vedea un grafic (precum cel de mai jos) cu trei butoane: "Zi", "Saptamana" si "Luna". Apasand pe oricare dintre aceste butoane, veti putea vedea evolutia preturilor pentru cautarea dvs.
     <p><img src="/assets/images/grafic_ajutor.jpg" alt="Grafic pentru anunturi imobiliare" /></p>
    </div>
  </p>
  <p id="filtru_pret">
    <h3 class="titlu_intrebare">Cum pot filtra rezultatele dupa pret?</h3>
    <div class="raspuns_intrebare">
   Rezultatele pot fi filtrate dupa pret adaugand in expresia de cautare cuvantul "pret". De exemplu putem cauta apartamente de doua camare in cartierul Militari ce au un pret de vanzare sub 45000 de EURO:
   <p><a href="index.php?cauta_text=2+camere+militari+pret<45000&amp;c=dm&amp;m=cauta">2 camere militari pret<45000</a></p>
   In cazul terenurilor filtrarea dupa pret se calculeaza la pretul pe metru patrat dupa cum urmeaza:
   <p><a href="index.php?cauta_text=teren+otopeni+pret<45&amp;c=dm&amp;m=cauta">teren otopeni pret&lt;45</a></p>
   Alte exemple:
   <p><a href="index.php?cauta_text=2+camere+militari+pret>50000&amp;c=dm&amp;m=cauta">2 camere militari pret&gt;50000</a></p>
   <p><a href="index.php?cauta_text=2+camere+crangasi+pret<45000&amp;c=dm&amp;m=cauta">2 camere crangasi pret&lt;45000</a></p>
   <p><a href="index.php?cauta_text=2+camere+berceni+30000<pret<45000&amp;c=dm&amp;m=cauta">2 camere berceni 30000&lt;pret&lt;45000</a></p>
   </div>
  </p>
  <p id="filtru_suprafata">
    <h3 class="titlu_intrebare">Cum pot filtra rezultatele dupa suprafata?</h3>
    <div class="raspuns_intrebare">
   Rezultatele pot fi filtrate dupa suprafata adaugand in expresia de cautare cuvantul "suprafata" sau prescurtarea "sup". De exemplu putem cauta apartamente de doua camare in cartierul Militari ce au o suprafata mai mare de 40 de metri patrati:
   <p><a href="index.php?cauta_text=2+camere+militari+sup>40&amp;c=dm&amp;m=cauta">2 camere militari sup&lt;40</a></p>
   Alte exemple:
   <p><a href="index.php?cauta_text=2+camere+militari+sup<50&amp;c=dm&amp;m=cauta">2 camere militari sup&lt;50</a></p>
   <p><a href="index.php?cauta_text=2+camere+crangasi+sup>40&amp;c=dm&amp;m=cauta">2 camere crangasi sup&gt;40</a></p>
   <p><a href="index.php?cauta_text=2+camere+berceni+30<sup<45&amp;c=dm&amp;m=cauta">2 camere berceni 30&lt;sup&lt;45</a></p>
   <p><a href="index.php?cauta_text=teren+otopeni+500<sup<1000&amp;c=dm&amp;m=cauta">teren otopeni 500&lt;sup&lt;1000</a></p>
   </div>
  </p>
